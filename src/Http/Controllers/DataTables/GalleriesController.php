<?php

namespace DesignByCode\Admin\Http\Controllers\DataTables;

use App\Http\Controllers\Controller;
use DesignByCode\Admin\Models\Gallery;
use Illuminate\Http\Request;

class GalleriesController extends DataTableController
{

    protected $allowDeletion = true;
    protected $searchable = true;


    public function builder()
    {
        return Gallery::query();
    }

    public function getDisplayableColumns()
    {
        return ['id', 'name', 'slug'];
    }

    public function getUpdatableColumns()
    {
        return ['name'];
    }

    /**
     * @param $id
     * @param Request $request
     */
    public function update($id, Request $request)
    {
        $request->validate([
            'name' => 'required|unique:galleries,name,' . $id
        ]);

        parent::update($id, $request); // TODO: Change the autogenerated stub
    }


}
